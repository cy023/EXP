/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  int chk = !(x ^ 0);
  int res = 0;
  chk = ~chk + 1;
  res += y & ~chk;
  res += z & chk;
  return res;
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
  int signx = (x >> 31) & 0x1;
  int signy = (y >> 31) & 0x1;
  int res = 0;
  res = !((y + ~x + 1) >> 31);
  res &= !((!signx) & (signy));
  res |= ((signx) & (!signy));
  return res;
}
