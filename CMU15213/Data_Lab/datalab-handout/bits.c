/* 
 * logicalNeg - implement the ! operator, using all of 
 *              the legal operators except !
 *   Examples: logicalNeg(3) = 0, logicalNeg(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int logicalNeg(int x) {
  int res = x | (x >> 16);
  res |= (res >> 8);
  res |= (res >> 4);
  res |= (res >> 2);
  res |= (res >> 1);
  return ~res & 0x1;
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  int sign = ~((x >> 31) & 0x1) + 1;
  int chk = 0;
  int res = 1;
  int shift = 0;

  chk = ((!!(x >> 16)) & (~sign)) | ((!!(~(x >> 16))) & (sign));
  shift = chk << 4;
  res += shift;
  x >>= shift;

  chk = ((!!(x >> 8)) & (~sign)) | ((!!(~(x >> 8))) & (sign));
  shift = chk << 3;
  res += shift;
  x >>= shift;

  chk = ((!!(x >> 4)) & (~sign)) | ((!!(~(x >> 4))) & (sign));
  shift = chk << 2;
  res += shift;
  x >>= shift;

  chk = ((!!(x >> 2)) & (~sign)) | ((!!(~(x >> 2))) & (sign));
  shift = chk << 1;
  res += shift;
  x >>= shift;

  chk = ((!!(x >> 1)) & (~sign)) | ((!!(~(x >> 1))) & (sign));
  shift = chk;
  res += shift;
  x >>= shift;

  res += !(x ^ 1) | !(x ^ ~1);

  return res;
}
